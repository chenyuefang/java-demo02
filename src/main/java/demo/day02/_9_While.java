package demo.day02;

public class _9_While {
    public static void main(String[] args) {
        int i=0;
        while(i<10){
            System.out.println(i);
            i++;
        }
        int i1 = 9;
//        System.out.println("-------------");
        do {
            System.out.println(i1);
            i++;
        } while (i1 < 10);
    }
}
